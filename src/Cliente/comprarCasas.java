    private void updateButtons(int c, int p, int d, boolean b) {
        System.out.println(b);
        if(b){
            if (d >= p && c < 5){
                comprar.setEnabled(true);
            }
            if(c == 0){
                vender.setEnabled(false);
            }
        }
        else{
            comprar.setEnabled(false);
            vender.setEnabled(false);
        }
    }
    public void opcionValida(int casas, int precio, int dinero, boolean can){
        System.out.println("Opcion valida");
        cant.setText(casas+"");
        this.precio.setText(precio+"");
        updateButtons(casas, precio, dinero, can);
    }
    public void opcionInvalida(){
        System.out.println("Opcion invalida");
        cant.setText("");
        this.precio.setText("");
        comprar.setEnabled(false);
        vender.setEnabled(false);
    }
