    private boolean isIn(ArrayList<String> names,JComboBox combo){
        System.out.println("Algo");
        for(int i = 0; i<names.size();i++){
            System.out.println("Está: "+names.get(i));
            if(names.get(i).equals(combo.getSelectedItem().toString())){
                System.out.println("Sí");
                return true;
            }
        }
        return false;
    }
    private void solicitarPropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solicitarPropActionPerformed
        aceptar.setEnabled(false);
        if (!isIn(propiedadesSolicitadas,susPropiedades)){
            propiedadesSolicitadas.add(susPropiedades.getSelectedItem().toString());
            actualizarTextField(propiedadesSolicitadas,listaPropiedadesAjenas);
        }   
    }//GEN-LAST:event_solicitarPropActionPerformed

