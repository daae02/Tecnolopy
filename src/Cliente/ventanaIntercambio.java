    private void sendAll() throws IOException{
            refPantalla.refCliente.hiloCliente.writer.writeInt(otroJugador);
            refPantalla.refCliente.hiloCliente.objWriter.writeObject(propiedadesOfrecidas);
            refPantalla.refCliente.hiloCliente.objWriter.writeObject(propiedadesSolicitadas);
            refPantalla.refCliente.hiloCliente.writer.writeInt(dineroOfrecido);
            refPantalla.refCliente.hiloCliente.writer.writeInt(dineroSolicitado);
    }
